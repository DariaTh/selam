                    'orderby' => array( 'menu_order' => 'ASC', 'title' => 'ASC' ),
                ));
                usort($agents, function (WP_Post $p1, WP_Post $p2) {
                    if ($p1->menu_order > $p2->menu_order) {
                        return 1;
                    }
                    elseif ($p1->menu_order < $p2->menu_order) {
                        return -1;
                    }
                    else {
                        $non_english_p1 = strlen($p1->post_title) != mb_strlen($p1->post_title, 'utf-8');
                        $non_english_p2 = strlen($p2->post_title) != mb_strlen($p2->post_title, 'utf-8');
                        if ( $non_english_p1 > $non_english_p2) {
                            return -1;
                        }
                        elseif ($non_english_p1 < $non_english_p2) {
                            return 1;
                        }
                        else {
                            if ($p1->post_title > $p2->post_title) {
                                return 1;
                            }
                            elseif ($p1->post_title < $p2->post_title) {
                                return -1;
                            }
                            return 0;
                        }
                    }
                });


                $i = 0;
